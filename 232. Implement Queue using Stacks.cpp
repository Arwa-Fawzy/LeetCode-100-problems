class MyQueue {
    stack<int>s1;
    stack<int>s2;
public:
    
    
    void push(int x) {
        s1.push(x);
    }
    
    int pop() {
        if(s2.empty()){
            while(!s1.empty()){
                s2.push(s1.top());
                s1.pop();
            }
        }
        int last=s2.top();
        s2.pop();
        return last;
    }
    
    int peek() {
        if(s2.empty()){
            while(!s1.empty()){
                s2.push(s1.top());
                s1.pop();
            }
        }
        int last=s2.top();
        return last;
    }
    
    bool empty() {
        return s1.empty()&&s2.empty();
    }
};

